	<h1>The Submissions to Richard II, 1395</h1>
	<p>In 1395, at what appeared to be the end of a protracted conflict with Art Ã“g MacMurrough and his allies, Richard II received the submissions of dozens of Irish lords. The submissions followed a well-established formula: the submitting parties prostrated themselves and paid homage to the king. They then swore an oath, usually in Irish, which was relayed through a trusted interpreter. Finally, they bound themselves to pay fines and other penalties should they break their oaths. While the particulars of the ritual sometimes varied from party to party, the overall consistency of the formula produced an invaluable resource in the accounts of the submissions. Transcribed and translated by Edmund Curtis in 1927, the notarial instruments offer a glimpse of the informal networks that exercised an often invisible influence on the ruling class of fourteenth-century Ireland.</p>
