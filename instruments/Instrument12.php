    <p>Notarial Instrument XII records that: on the 6th day of April, 1395, in a certain room within the close of the Friars Preachers near Kilkenny, in the presence of the King and the notary and other witnesses, Moris fitz Moris of the Geraldines, an English knight born in Ireland, removing his girdle and cap, on bended knees, &c., took these words in the English language, viz.:</p>
<p>‘I, Moris fitz Moris, become liegeman of Lord Richard, King of England and France and Lord of Ireland, sovereign lord of me and my nation, &c., [as in Instrument I], so help me God and these God’s holy Gospels.’</p>
