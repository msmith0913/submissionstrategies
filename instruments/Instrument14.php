    <h1>Notarial Instrument 14</h1>
    <h2>The Letter of Bernardus O'Brien</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XIV records that: ‘on the 4th day of February, 1394 [N.S. 1395], in a chapel of King Richard’s ordained for celebrating Mass in the great church within the monastery of St. Thomas the Martyr in Dublin, in presence of the notary and other witnesses, King Richard required me to make him a public instrument containing a true copy of certain letters as follows:</p>
<p>‘”To the most excellent our lord the King, greeting with all humility, reverence, and honour. Most serene lord, if I had known that your most excellent Lordship would have deigned graciously to receive my letters, I would have written to you the moment you set forth for your land of Ireland, heartily and [164] humbly with all subjection submitting myself and all mine to your illustrious grace, wherefore in this regard deign to hold me excused; and if I have sinned in anything against your most noble Lordship or my allegiance, I will compensate and amend to your wish and honour and at my cost and labour and whatever else may be acceptable to your Majesty; and for my fealty and perpetual observing of peace to you and yours I will bind to the full myself and all who are subject to me in sufficient pledges and other obligations befitting to your Majesty; considering, if it please your Majesty, that among all the English and Irish of your land of Ireland I have acquired neither lands nor possessions by conquest nor made any profit save such as your predecessors, most illustrious kings of England, granted and gave to my predecessors together with their arms and standards and other liberties as in the future shall be more fully declared to you and your most sage Council; and if any English and Irish should seek to make any complaint against me in anything, I would do full justice, by our common and ancient usages practised to this day amongst us, in your illustrious presence or that of another assigned for that purpose by you, and who shall write back by the bearer of these what is your Majesty’s pleasure in these and other things.”’</p>
<p>And the superscription of the letter contains these words:</p>
<p>‘”Your humble Berandus [Bernardus or Brian] O’Brien, Prince of Thomond”, which letter, sealed with red wax, and containing three leopards, as others said, I myself saw and handled.’</p>
<p>Witnesses: Richard Scrope, Chamberlain of the King, and William Arundel, knights.</p>
