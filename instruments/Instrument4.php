  <h3>Submission text (English, translated by Edmund Curtis)</h3>
  <p>Notarial Instrument IV records: that on the 21st day of April, 1395, in the convent church of the Friars Preachers in the suburbs without the walls of Kilkenny in the presence of King Richard and of the notary and other witnesses</p>
<p>‘Arthur de Demesey in Leinster [Art O’Dempsey] in person, with a cord bound around his neck and the palms of his hands joined together, prostrated himself of his free will at the feet of the said Lord King with body prone to the earth, and on account of his ill doings unlawfully committed fearing that his guilt could not be purged by the imprisonment of the body, humbly asked forgiveness. Then the said Lord King, suffused with the glow of mercy, and wishful that no honest intention of his should go awry, moved thereto by the venerable Father in Christ Robert, by the grace of God archbishop of Dublin, who, kneeling before the King, made frequent prayers on his behalf for his lawless deeds frequently committed against the English—and overflowing with pity and so that no turbulent pride in his former crimes should assert itself, but that his various deeds, adorned with virtuous conduct, should shine for future ages throughout the universal climes of the world—granted life and mercy to the said Arthur de Demesey, who gratefully took these words in Irish, which were rendered in English by John Malachy, interpreter, in the presence of many well understanding the Irish language, viz.:</p>
