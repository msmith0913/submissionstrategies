    <h2>The Submission of Art MacMurrough</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXII records that: on the 14th day of April, 1395, near the cross commonly called O’Brenanscross, near Castlecomer, before Richad, bishop of Ossory, in presence of the notary, &c., came Art MacMurrough, who, not coerced nor induced by fear, but of his own free will, swore on the holy Gospels that for the future he will well and faithfully observe all and each of the obligations and conventions made by instruments and indentures between the Earl of Nottingham on behalf of King Richard and the said Arthur, howsoever agreed and concluded, according to the tenor of the said instruments or indentures made between the two, and fulfil them without reserve. And if he fail to observe the said agreements or conventions or shall in part or in whole violate them, he admits that he will incur the penalties specified in the said instruments or indentures; also with regard thereto and to the premises all and single, he submitted himself unreservedly to the ordinance, disposition, coercion, and compulsion of Robert, archbishop of Dublin, and [186] of the bishop of Ossory and of their successors who shall be for the time being, whatsoever they or either or any of them shall decree or ordain concerning the same Art, waiving all appeals and remedies of law whatsoever, which were there and then expressly renounced by him.</p>
<p>Witnesses: Fergal O’Kurryn, vicar of the parish of the church of Tamlyn, Robert Shortal and John Saykyn, of Leighlin, Ossory, and London dioceses.</p>
<p>‘And I, Robert Boleyne, clerk, &c., was present, &c. [as in Instrument V], and have appended here my sign wonted and accustomed, and have also appended the seal of the said Art MacMurrough, thereto requested by him, in faith and witness of the premises.’ </p>
