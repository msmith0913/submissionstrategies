    <h1>Notarial Instrument 11</h1>
    <h2>The Submission of Adam Tobyn</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XI records that: on the 3rd day of April, 1395, in a dry place within the house of the Friars Preachers near the town of Kilkenny, in the presence of the notary, &c., King Richard made a statement that:</p>
<p>‘Adam, son of Richard syn Tobyn [“de sancto Albano”] of Cashel diocese, an Englishman of Ireland and a rebel, fell before his feet with bended knees and his head bowed to earth and a cord bound about his neck, fearing justly to be condemned to death, and confessing himself on account of his offences worthy to be punished by death; submitting also his body and all his goods movable and immovable to the royal will, asking for mercy and grace. Whereupon the King, moved by a great [162] glow of pity, inclining mercifully to the prayers of petitioners made frequently on his behalf and induced by great pity, with every readiness to forgive, granted him to have his life free from the peril of death.’</p>
<p>Whereupon the King requested the notary to make a public instrument, the notary not having been present at the above submission.</p>
<p>Witnesses: R., Archbishop of Dublin, the bishops of Chichester, Llandaff, and Waterford-Lismore, and John Boor, dean of the free royal chapel.
</p>
