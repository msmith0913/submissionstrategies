    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXVI records that: on the 10th day of March, 1394 [N.S. 1395], in a certain room of the Friars Preachers in Drogheda, in presence of the King, the notary, and others, lord Shane MacMahon, Irishman, of Clogher diocese, removing his cap and on bended knees, &c., took these words in Irish, which were rendered into English, so the notary learns, by Stephen Garnon, interpreter, of Armagh diocese, viz.:</p>
  <p>‘I, Shane MacMahon, become liegeman of my lord King Richard’, &c. [as in Instrument I, to the words ‘any of them ’, continuing ‘and I will come in person to your parliament whenever I shall be [189] called or summoned in any manner by you or some other in your name, all delay put aside’]. For the observing of which, he took corporal oath and bound himself in £1,000 sterling. Thereupon the King admitted him to kiss hands as his liegeman, and Shane requested the notary to make him a public instrument.</p>
