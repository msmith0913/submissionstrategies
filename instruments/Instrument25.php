    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXV records that: on the 14th day of March, in a certain room of the Friars Preachers at Drogheda, in the presence of the King, the notary, and others, Morgh [Murchadh] Ban O’Farrel and Ross O’Farrel of Ardagh diocese, Melaghlin Mageoghegan, and Niall O’Molloy of the county and diocese of Meath, famous Irishmen, removing their belts, daggers, and caps, on bended knees, &c., took each these words in Irish, so the notary learns from the lips of John Bocombe, interpreter, and others well understanding Irish, viz.:</p>
    <p>‘I become liegeman of my lord King Richard’, &c. [as in Instrument I]. For observing which, each of them singly took corporal oath. Thereupon the King admitted them each as his liegeman, and they requested the notary to make them public instruments.</p>
