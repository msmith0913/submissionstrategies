    <h1>Notarial Instrument 36</h1>
    <h2>The Submission of Donal Morghysson</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXXVI records that: ‘on February 2,1394 [N.S. 1395], in a certain room of the Earl of Rutland and Cork within the abbey of St. Thomas the Martyr in the city of Dublin, in the presence of the notary, Donal Morghysson, of Wexford diocese, admitted himself to be true liege of lord Richard, King of England and France and Lord of Ireland, from that day forth for ever, also of his heirs and successors, being kings of England, in life, limb, and earthly honour, so that he and they shall have over him power of life and death, and that he will aid in defending the King and his heirs against all earthly enemies with all his power and that of his men, and he will be obedient to the laws, commands, and ordinances of any or all of them, and he will do in all and singular that which a good and faithful liegeman owes and is bound to do to his natural liege lord, so God aid him and these God’s holy Gospels.</p>
<p>‘For observing which oath of allegiance, &c., he took corporal oath. And the said Donal Morghysson did liege homage on the preceding day to the Lord King, as he asserted, but because I, the subscribed notary, was not then present, the King ordered me to receive the oath of homage from the said Donal. Whereupon the said Donal requested me to make him a public instrument.’</p>
<p>Witnesses: Robert, bishop of Waterford-Lismore, Lord William de Angel, knight, and William Clynton, esquire, of Poitou and Chester dioceses.</p>
