    <p>Notarial Instrument XVIII records that: on the 1st day of March, [1395], in a room of the King’s in the abbey of St. Thomas Martyr in the suburbs of Dublin, in presence of the notary, &c., ‘Bernard [Brian] O’Brien of Killaloe diocese, before our most excellent lord King, removing his girdle, sword, and cap and on bended knees, &c., [as in Instrument I], took these word sin Irish which I learned from the recitation of Thomas [James] Butler, Earl of Ormond, the interpreter, and one well skilled in the Irish speech, and of others well understanding Irish, and swore to be true liegemen of our Lord the King and obedient to all his laws and ordinances, viz. “I, Brian O’Brien, captain of my nation, swear to be good and faithful liegeman of our Lord Richard, King, &c., and Lord of Ireland, his heirs, and successors, being kings of England, and I will be obedient to his and their laws, commands, and ordinances” [as in Instrument I].’ For observing of which allegiance, he took corporal oath and bound himself in 20,000 marks of English money. Whereupon the King admitted Brian, Prince of Thomond, [182] to the kiss of peace as his liege, and the said Brian requested the notary to make a public instrument.</p>
