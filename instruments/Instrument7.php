  <h1>Notarial Instrument 7</h1>
  <h2>The Letter of Tatheus MacCarthy</h2>
  <h3>Letter text (English, translated by Edmund Curtis)</h3>
  <p>Notarial Instrument VII records that: on the 8th day of March [1395], in a room within the house of the Friars Preachers at Villa Pontana alias Drogheda in the presence of the notary and other witnesses, King Richard showed the notary a letter sealed with red wax sent by Tatheus [Taig or Tadhg] MacCarthy, Prince of the Irish of Desmond, with the intent to do liege homage, and handed it to the notary, requiring him to make him a public instrument containing a true copy of the letter, the tenor of which was as follows:</p>
<p>‘Most excellent Lord, I humbly recommend myself and all mine, in so far as I am worth anything, to your Lordship, signifying to your Majesty that my ancestors from the time of the Conquest of your land of Ireland, and I also all my life, have been the men of you and your ancestors, attempting nothing ever against your royal Majesty or your lieges which might turn to the prejudice of you or your lieges, nor for the future do I intend in any manner to attempt such ; nor do I obtain [i. e. hold] any possessions save those which from of old I have obtained under your dominion and that of my lord, the Earl of Desmond. Considering that at present in the parts of County Cork I have been labouring at a certain parley with a kinsman of mine, knowing nothing of the matter moved against me by the Earl of Ormond on the part of your Majesty, by reason of which [parley] I am not able to set forth to your [159] presence so suddenly without great danger to my country, but, God permitting, when you shall have approached the parts of Munster, with a joyful mind and without delay will I come to you, and whatever I can do for the benefit of your worshipful Lordship that gladly will I do. Wherefore if it please your Lordship, deign to excuse me, your liegeman, for the above cause, from cming to you on this occasion, imploring the favour of your gracious Lordship, seeing that never did I at the incitement of other Irishmen of your land rise against you or yours contrary to my allegiance. Written at Ballaghath, 13th of February [1394-5]. Your Tatheus Makarthy, Prince of the Irish of Desmond.’</p>
<p>Witnesses: Richard, bishop of Chichester, and John Golafre, knight.
</p>
