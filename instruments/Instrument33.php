    <h2>The Submission of Turloch O'Brien</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXXIII records that: on the 25th day of April, 1395, in the church of the Friars Preachers at Kilkenny, in the presence of King Richard, the notary, and others, Turloch, son of  Murchadh O’Brien of Munster, removing his mantle, girdle, and cap, and on bended knee, took these words in Irish, which were rendered in English by William O’Cormacan, bishop of Clonfert in Connacht, viz.:</p>
<p>‘I, Turloch, &c., become liegeman’, &c. [as in Instrument I]. For observing which, he bound himself in 20,000 marks. Whereupon the King admitted him as his liegeman. Then came Thomas O’Dwyer, Philip, son of Matthew [? Mahon] Don O’Kennedy of Munster, and Niall O’Molloy, ‘son of Rory’ of Meath, and did liege homage ; the first two bound themselves in £1,000 each and the third in 1,000 marks, and their interpreter was Thomas Kildare, mayor of the city of Limerick. Whereupon they requested the notary to make them public instruments.</p>
