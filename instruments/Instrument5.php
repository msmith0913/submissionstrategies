  <h1>Notarial Instrument 5</h1>
  <h2>The Submission of the Reeves and Tenants of Art MacMurrough</h2>
  <h3>Submission text (English, translated by Edmund Curtis)</h3>
  <p>Notarial Instrument V records: that on the 1st day of April, 1395, in the town of New Leighlin, before Lord John Stanley, sent by Richard, by the grace of God King of England and of France and Lord of Ireland, in the presence of the recording notary and the subscribing witnesses:</p>
<p>‘William Beg, John Nicholson, and Maurice Adalagh, who were reeves of Art MacMurrough styling himself lord of certain lands and possessions of a part of Leinster as they there openly and publicly asserted, also Malachy Cam and John Glasmorgh, tenants of the said part of Leinster, not compelled or induced by fear, but sincerely, spontaneously and freely as it appeared to me, on God’s holy Gospels bodily touched by them and each of theme there, swore each and every one that for the future they will be faithful liegemen of our [156] lord King Richard and all his successors in the kingdom of England and of his and their lieutenants in the land of Ireland, and do exercise their office of reeve in the said country before the Lord Marshal, Earl of Nottingham, their lord as it is asserted, and will hold themselves and each of them well and faithfully in the said office before the Earl Marshal, and will not suffer any malefactors or rebels or disturbers of the peace of any kind in the said part of the land of Leinster, but will one and each to their utmost power resist the same and pursue and oppose the said malefactors, if such there be, according to the laws and statutes of the realm of England issued from thence as the said statutes made against such demand and require.’</p>
<p>Witnesses: Fergal O’Kurryn, vicar of the parish church of Tamlyn, interpreter of the said oath to the said Irishmen in the vulgar speech of the Irish, Robert de Orrey, and Henry Bekirton, respectively of Leighlin, Coventry, and Lichfield dioceses.</p>
<p>Affirmation of the notary, Robert Boleyne, clerk of Ely diocese, that he was present together with the witnesses and that he has drawn up an instrument accordingly and appended his seal.</p>
