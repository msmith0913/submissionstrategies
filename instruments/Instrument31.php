    <h2>The Submission of Murchadh O'Conor Faly</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXXI records that : on the 28th day of March, 1395, in the great hall within the abbey of Conall, in the presence of the King, the notary, and others, Murchadh O’Conor Faly, captain of his nation, removing his girdle, sword, and cap, and on bended knee, took these words in Irish, which were rendered in English by lord William Wellesley, knight, of Kildare, well skilled in both the Irish and English languages, viz. :</p>
<p>‘I, Murchadh O’Conor Faly, become liegeman’, &c. [as in Instrument I]. For observing which allegiance and keeping all conventions, &c., made between the King on one side and by Art MacMurrough and Gerald O’Byrne on the other, he took corporal oath. Thereupon the King admitted him as his liege, and he requested the notary to make him a public instrument.</p>
