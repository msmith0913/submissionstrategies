    <h2>The Submission of Taig and Donal MacCarthy</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXX records that: on the 6th day of April, in the great church of the Friars Preachers at Kilkenny, in the presence of King Richard, the notary, and others, Taig [Tadhg] MacCarthy Mor, removing his girdle, dagger, and cap, on bended knee, took these words in Irish, which were rendered in English by lord Maurice FitzMaurice, knight, viz. :</p>
<p>‘I, Taig MacCarthy, become liegeman of my lord King Richard’, &c. [as in Instrument I]. For observing which, he bound himself in 20,000 marks. Whereupon the King admitted him to the kiss of peace as his liege. Then came Donal MacCarthy and did liege homage in the same form and bound himself in 20,000 marks. Thereupon they requested the notary to make them public instruments.</p>
