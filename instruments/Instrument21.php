    <h2>The Submission of Cologh Magennis et al</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXI records that: on the 19th day of March, 1394 [N.S. 1395], in the church of the Friars Minor near the great altar in the choir, in the town of Dundalk, in the presence of King Richard and the notary, &c., ‘Cologh [Cu Uladh] Magennis, removing his cap and girdle and on bended knees, took these words in Irish, which were rendered in English by John Faltagh, in the presence of many well understanding Irish, viz.:</p>
<p>‘”I become the liegeman of my lord King Richard”, &c. [as in Instrument I]. For the observing of which allegiance he took corporal oath on the cross of John, Primate of Ireland. Then the King admitted Cologh as his liegeman. Then came Adam MacGilmore of Down diocese, and did homage likewise, and John Faltagh was his interpreter. Then came Philip MacMahon of Oriel [Clogher] diocese, and did homage, and his interpreter was Walter Teling of Armagh diocese. Then came Odo [185] [Aedh] MacMahon of Clogher diocese and did homage, and his interpreter was Master Thomas O’Loghre of Clogher diocese. Then came MacCabe of Clogher diocese and did homage, and his interpreter was Walter Teling. Each of these swore on the Bible, but Cologh Magennis swore on the Archbishop’s cross. Then the aforesaid Cologh, &c., requested me to make them public instruments.’</p>
