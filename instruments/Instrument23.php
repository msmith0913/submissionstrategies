    <h1>Notarial Instrument 23</h1>
    <h2>The Submission of Turloch O'Conor Don, William de Burgo, and Walter Bermingham</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXIII records that: on the 1st day of May, 1395, on a ship of the Lord King called the Trinity, then in the port of Waterford, in the presence of the notary and others, the King being there in person together with the bishops of Chichester and Lismore-Waterford, there came Turloch O’Conor Don of Connacht, William de Burgo, and Walter Bermingham (who formerly, it was said, were rebels against the King), who came on board and lying prone there did obeisance. So the King, seeing that they had come to him and desiring that they should not leave him without some gift or honour, created them and belted them knights, and as token of that Order, admitted them to the kiss of peace and granted each of them a sword to be honourably used. And immediately thereupon the lords Henry Percy and William Arundel, knights, [187] placed gilded spurs upon their heels as token of that order. Then the said Turloch O’Conor Don, William de Burgo, and Walter Bermingham paid the honour due to such a king, kneeling and with uplifted hands. Whereupon the King ordered the notary to make him a public instrument.</p>
<p>Witnesses: John Boor, dean of the King’s chapel, John Borghulle, the King’s confessor, and Thomas Merke, monk of Westminster.</p>
