  <h1>Notarial Instrument 3</h1>
  <h2>The Submission of Donnchadh O'Byrne</h2>
  <h3>Submission text (English, translated by Edmund Curtis)</h3>
  <p>Notarial Instrument III records: that on the 18th day of February, 1394 [N.S. 1395], in a room within the castle of Dublin belonging to King Richard, in presence of the notary and other witnesses:</p>
<p>‘A certain Irishman, lately enemy and rebel of our Lord the King, Donatus O’Bryn [Donnchadh O’Byrne] by name and nation, came and approached, not compelled nor forced but sincerely and freely, and swore on the holy Gospels, which he touched with his hands and kissed, to be from that time hence- [153] forth faithful liegeman to our Lord the King and to faithfully adhere to and serve and obey him as his liege lord against all men as every faithful man is bound to his liege lord, with every kind of submission, services, obedience and fealty, and to keep his laws, commands and precepts, and continually to obey them without any complaint, and to suffer, sustain and inviolably preserve his ordinances, constitutions and statutes and constantly to persevere in them fully and wholly for the future throughout his life, and not to swerve from them in anything, but to faithfully observe and with all his power by God’s aid to fulfil all and sundry and whatsoever things else, no matter how many or of what kind, which a faithful liege subject and vassal is bound and held to do and perform to his liege lord with all his power, by God’s aid – and he did fealty and homage in and for all things, fully submitting himself for all his crimes and delicts to the correction of our Lord the King and his deputies sent for this purpose.’</p>
<p>Witnesses: Robert, Archbishop of Dublin, Primate of Ireland and Chancellor, Richard, bishop of Chichester, Treasurer, Robert, bishop of Lismore and Waterford, Lord William Lescrop, King’s Chamberlain, Simon Felbryge, standard-bearer, and Baldwin Bereford, knights, and Thomas Mareward, Mayor of Dublin, and David Bertevyll, sergeant-at-arms of our Lord the King, with a great number of clerks, knights and squires.</p>
<p>Affirmation of the notary, William Bernard of ‘Insula Gernon’ in the diocese of Constance, that he was present at the taking of the oath of homage [154] and fealty, rendered by touching and kissing the Gospels.</p>

