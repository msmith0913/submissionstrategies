    <h1>Notarial Instrument 20</h1>
    <h2>The Submission of Taig O'Carroll</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XX records that: on the 20th day of April, 1395, in the church of the Friars Preachers at Kilkenny, King Richard showed and [183] handed to the notary a letter sealed with red wax sent to him by Taig O’Carrol for the sake of obtaining pardon and requested him to make a public instrument containing a true copy of the letter, as follows: ‘Let it be fully known and patent to your Majesty that I, together with my predecessors, have been obedient and devoted to your Magnificence; and now, God be witness, I declare that I have stood forth firmly and signally as your special subject with all the reverence and honour due in the accustomed manner to your excellence, and in perfect submission I intend to follow your royal standard and defend it with armed hand against all your enemies. Therefore did my predecessors in sign of submission, reverence, and subjection made to your predecessors, on bended knees hand over to them their son in the flesh as special hostage; even so with you in your royal palace still resides and lives, it appears, such a hostage, formerly our subject, William O’Carrol, and I myself have given to your lieutenants in token of submission my two sons in the flesh, and have surrendered myself and my men in perpetual servitude to your most excellent power, from which it follows therefore that by the law of lordship I am your immediate servant and special subject absolutely exempt from the lordship of other Irishmen. I ask of your eminence—whose magnificent deeds resound through the whole world, which protects and defends the orphans and gives ready heed to the widows seeking justice, which is ever inclined to take away the griefs of subjects, which I am bound above all things to revere both humbly and dutifully—that you should of your [184] grace and mercy not bind me to appear personally before you in the midst of my enemies, because I cannot prevail against my powerful enemies nor am I able, having taken counsel with my friends, to present myself to you, nor is it in contempt of you but through hatred and fear of them that I have kept away from your Grace. I admit you then to be my immediate lord and no other.</p>
<p>‘Given at Roscrea on the 6th day after Easter [17 April], your true and devoted subject Taig O’Carrol, captain of his nation.’</p>
<p>Witnesses: R., Archbishop of Dublin, Thomas Merke, monk of Westminster.</p>
