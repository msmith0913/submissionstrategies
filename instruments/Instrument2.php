  <p>Notarial Instrument II records: that on the [152] 16th day of March, 1394 [N.S. 1395], in the same place as above in Drogheda, in the presence of the King:</p>
<p>Catholicus [CÃº Uladh] son of 'Avyrcagh' O'Hanlon, removing his girdle, mantle and cap, on bended knees fell at the feet of the King and raising his hands with the palms joined together and holding them erect between the hands of the King, took these words, which were rendered into English by Thomas Talbot, interpreter, well skilled in the Irish language, in the midst of many, lay and cleric, well understanding Irish, viz.:</p>
