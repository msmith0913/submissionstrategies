  <h3>Submission text (English, translated by Edmund Curtis)</h3>
  <p>Notarial Instrument VI records that: on the 16th day of April, 1395, in the convent church of the Friars Preachers outside the walls in the suburbs of Kilkenny in the presence of King Richard:</p>
<p>‘Tatheus O’Mare [Taig O’Meagher] of Munster, captain of his nation, removing his girdle, &c., and on bended knees, &c. [as in Instrument I], took these words in Irish, which were rendered in English by [157] John Malachy, chaplain, interpreter, in the presence of many, both cleric and lay, well understanding the Irish language then standing around, viz.: </p>
