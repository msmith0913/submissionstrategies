<p>‘This done, came Terrelagh O’Concor de Connacia [Turloch O’Conor of Connacht] and did liege homage to the Lord King delivered under the same form. And for the observing and affirming these allegiances and fealties towards the Lord our King as their sovereign lord, and his heirs being kings of England, having touched and kissed the holy Gospels, they took corporal oaths, and further by a similar oath, the said T. O’Concor bound himself that if, which God forbid, he shall violate his said oath in part or in whole, doing or procuring to be done anything by himself or others in contempt of the said Lord King or his heirs or to the offence of himself or his heirs or to the detriment of his government or that of his heirs, he will pay to the Papal Camera 20,000 marks of good and lawful sterling English money.’</p>
<p>The King thereupon admitted Taig O’Meagher and Turloch O’Conor as his lieges and they requested the notary to make them public instruments.</p>
<p>Witnesses: Tideman and Robert, bishops of Llandaff and Lismore-Waterford, John Holland, Earl of Huntingdon, and William Scrope, the King’s Chamberlain.</p>
<p>Affirmation of Thomas Sparkeford, clerk of the diocese of Bath and Wells, public notary, that hew as present with the other witnesses and has drawn [158] up an instrument in public form accordingly and appended his seal.</p>
