    <h1>Notarial Instrument 34</h1>
    <h2>The Letter of Turloch O'Conor</h2>
    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument XXXIV records that : on the 21st day of April, 1395, in a room of the Friars Preachers at Kilkenny, in the presence of the notary, &c., King Richard exhibited a letter sealed with red wax sent to the King by Theotricus [Turloch] O’Conor, Lord of Connacht, a famous Irishman, and handed it to the notary and requested him to make a public instrument containing a true copy of the letter, the tenor of which was as follows :</p>
<p>‘“To the illustrious Prince his lord, Richard, King of England and France, and Lord of Ireland, homage, submission, and greeting. In the beginning of this letter I recommend myself to your royal Majesty as true liege. Let your Majesty know that all the chief Irish of Connacht, a few only excepted, have been up to this, and now are, subject, with the permission of your royal Majesty, to me, as true and lawful heir of my predecessors; to whom formerly your predecessors gave lands and lordships, and even to this day I possess the greater part of these grants according as they were granted, although another of my race, Turloch Ruadh by name, wishful with the aid of some Irishmen who have since been expelled by me to appropriate to himself my lordship and that of my ancestors (though he is a bastard and son of a bastard), has aspired to the name and title by which I am called in Irish fashion, that is to say O’Conor; a man of little power, he possesses some pieces of land with my permission near to my lordship. Please give no credence to his messengers until my coming to you. And I will hasten without fail to see your royal Majesty, if you approve thereof (though it is against the wishes of some Irishmen of my lordship), in the company of James, Earl of Ormond, who speaks both languages fluently. Also I humbly beseech your royal Majesty to deign to inform me in writing by the bearer of these presents whatever maybe your wish that I should do as regards the above matters. May your royal Majesty prosper and flourish with vengeance over your enemies to the government of your realms and lordships.</p>
<p>‘“Written at Roscommon the 3rd day of April. Your ever humble obedient subject, Turloch O’Conor Mor, Lord of the Irish of Connacht.”</p>
<p>‘Which letter I, the notary, saw, handled, and perused and drew up into this true copy in instruments.’</p>
<p>Witnesses: Nicholas Slake, archdeacon of Wells, and Thomas Merke, monk of Westminster.</p>
