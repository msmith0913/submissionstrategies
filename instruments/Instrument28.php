    <p>Notarial Instrument XXVIII records that: on the 30th day of March, 1395, in a meadow situated between the castle of Carlow and a certain bank of the river, in the presence of King Richard, the notary, [190] and others, Thomas Carragh Kavanagh, of Leighlin diocese, removing his girdle, sword, and cap, on bended knees took these words in Irish, which were rendered into English by Richard Nelgan, interpreter, viz.: ‘I, Thomas Carragh Kavanagh, become liegeman’, &c. [as in Instrument I].</p>
