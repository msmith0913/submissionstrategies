<p>Then came David More O’Morgh [O’Morchoe or O’Murchadha] and Gillapatrick Rua O'More and did liege homage similarly, except that Gillapatrick on account of corporal infirmity did his liege homage on horseback to the King, who similarly was seated on his horse. Then on the same day at Leighlinbridge came Donal O'Nolan to the door of the church of the Carmelites at Leighlin bridge and did liege homage to the King in the same manner, his words being faithfully rendered from Irish into English by Simon Vale, interpreter.</p>
<p>For observing which, and keeping faithfully all conventions made between the King on one side and Art MacMurrough and Gerald O’Byrne on the other, each singly took corporal oath on the holy Gospels. Whereupon the King admitted them as his lieges and they requested the notary to make them public instruments.</p>
