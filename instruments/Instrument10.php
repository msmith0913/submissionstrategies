    <h3>Submission text (English, translated by Edmund Curtis)</h3>
    <p>Notarial Instrument X records that: on the same day, the 16th of March, and in the same place in Drogheda, in the presence of the King and notary, Neel O’Hanloyn [Niall O’Hanlon], removing his girdle, &c., and on bended knees, &c., took these words in Irish, which were rendered in English by Thomas Talbot, interpreter, in the presence of many understanding Irish, viz.:</p>
<p>‘I, Niall O’Hanlon, become liegeman of my Lord Richard’, &c. [as in Instrument I]. </p>
