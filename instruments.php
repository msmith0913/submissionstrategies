    <h1 id="Annals">CELT Texts</h1>
    <p>Many of the surviving annals were edited by scholars like John O'Donovan. Those editions have been further edited and prepared for the web by the <a href="https://celt.ucc.ie">Corpus of Electronic Literature (CELT)</a> at University College Cork. Here I've provided bibliographic information for both the printe and online editions.</p>
      <ul>
        <li><strong>AC: </strong><a href="https://celt.ucc.ie//published/T100011/index.html">A. Martin Freeman, ed., Annála Connacht: The Annals of Connacht (Dublin: Dublin Institute for Advanced Studies, 1944), electronic edition compiled by Pádraig Bambury.</a></li>
        <li><strong>AFM: </strong>John O’Donovan, <i>Annala Rioghachta Eireann: Annals of the kingdom of Ireland by the Four Masters, from the earliest period to the year 1616</i>, 7 volumes (Dublin: Hodges and Smith, 1848-1851).<ul>
